	public static Path createFile(String path_of, String pathOfDirectory, String name) {
		Path path = Path.of(path_of);
		Path directory = Path.of(pathOfDirectory);
		if (!Files.exists(path)) {
			try {
				path = Files.createFile(directory.resolve(name));
			} catch (IOException ex) {
				Logger.getLogger(Mian.class.getName()).log(Level.SEVERE, null, ex);
				return null;
			}
		}
		return path;
	}

	public static void writeFile(Path path, String to_write) {
		try {
			Files.writeString(path, to_write, StandardCharsets.UTF_8);
		} catch (IOException ex) {
			Logger.getLogger(Mian.class.getName()).log(Level.SEVERE, null, ex);
		}
	}

	public static void createAndWriteFile(String path_of, String path_of_directory, String name, String to_write) {
		Path path = createFile(path_of, path_of_directory, name);
		if (path != null) {
			writeFile(path, to_write);
		}
	}

