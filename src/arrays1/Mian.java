	public void exercise67strings(JTextField jTextField, JLabel label) {
		String input = jTextField.getText();
		String output = input;

		// Formating a input string
		for (int i = 0; i < input.length(); i++) {
			Integer number = tryParse(String.valueOf(input.charAt(i)));
			if (number != null) {
				// Enter a space
				output = replaceAt(input, i, number.toString() + " ");
			}
		}

		label.setText(output);
	}

	public static String replaceAt(String str, int index, String ch) {
		return str.substring(0, index) + ch + str.substring(index + 1);
	}

	public static Integer tryParse(String text) {
		try {
			return Integer.parseInt(text);
		} catch (NumberFormatException e) {
			return null;
		}
	}

	public static int getMinOfArray(int[] array) {
		int min = Integer.MAX_VALUE;
		for (int i : array) {
			min = Math.min(min, i);
		}
		return min;
	}

	public static int[] getPostionOfAbsMin(int[][] array) {
		int[] result = new int[2];
		int min = Integer.MAX_VALUE;

		for (int i = 0; i < array.length; i++) {
			for (int j = 0; j < array[i].length; j++) {
				if (Math.abs(array[i][j]) < Math.abs(min)) {
					result[0] = i;
					result[1] = j;
					min = array[i][j];
				}
			}
		}

		return result;
	}

